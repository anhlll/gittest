    private void tblMSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMSMouseClicked
        // TODO add your handling code here:
        int row = tblMS.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = tblMS.getValueAt(row, 0).toString();
        String ma = tblMS.getValueAt(row, 1).toString();
        String ten = tblMS.getValueAt(row, 2).toString();

        txtidsp.setText(id);
        txtmams.setText(ma);
        txttenms.setText(ten);
    }//GEN-LAST:event_tblMSMouseClicked

