    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        MauSacViewModel sp = new MauSacViewModel();
        String ma = txtmams.getText();
        String ten = txttenms.getText();
        if (ma.isEmpty() || ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bổ trống");
            return;
        }

        sp.setMams(ma);
        sp.setTenms(ten);

        if (mauSacService.insert(sp)) {
            JOptionPane.showMessageDialog(this, "ok");
            loadData(mauSacService.getList2());
        } else {
            JOptionPane.showMessageDialog(this, "No");
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        MauSacViewModel sp = new MauSacViewModel();
        String ma = txtmams.getText();
        String ten = txttenms.getText();
        String id = txtidsp.getText();
        if (ma.isEmpty() || ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bổ trống");
            return;
        }

        sp.setMams(ma);
        sp.setTenms(ten);
        sp.setId(id);

        if (mauSacService.update(id, sp)) {
            JOptionPane.showMessageDialog(this, "ok");
            loadData(mauSacService.getList2());
        } else {
            JOptionPane.showMessageDialog(this, "No");
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        String id = txtidsp.getText();

        if (mauSacService.delete(id)) {
            JOptionPane.showMessageDialog(this, "ok");
            loadData(mauSacService.getList2());
        } else {
            JOptionPane.showMessageDialog(this, "No");
        }
    }//GEN-LAST:event_btnxoaActionPerformed

