    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        KhachHang kh = new KhachHang();
        String ma = txtmakh.getText();
        String ten = txttenkh.getText();
        String ho = txtho.getText();
        String sdt = txtsdt.getText();
        Date ns = Date.valueOf(txtns.getText());
        String sdienthoai = txtsdt.getText();
        String diachi = txtdiachi.getText();
        String ThanhPho = txtthanhpho.getText();
        String Quocgia = txtquocgia.getText();
        String mk = txtmatkhau.getText();

        kh.setMakh(mk);
        kh.setTenkh(ten);
        kh.setHokh(ho);
        kh.setSdt(sdt);
        kh.setNgaySinh(ns);
        kh.setDiaChi(diachi);
        kh.setThanhPho(ThanhPho);
        kh.setQuocGia(Quocgia);
        kh.setMk(mk);
        kh.setSdt(sdienthoai);

        if (khachHangService.insert(kh)) {
            JOptionPane.showMessageDialog(this, "ok");
            loadData(khachHangService.getList());
        } else {
            JOptionPane.showMessageDialog(this, "No");
        }
    }//GEN-LAST:event_btnthemActionPerformed

