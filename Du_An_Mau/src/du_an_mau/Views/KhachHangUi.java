    public void loadData(ArrayList<KhachHang> list) {
        defaultTableModel = (DefaultTableModel) tblkh.getModel();
        defaultTableModel.setRowCount(0);

        for (KhachHang kh : list) {
            defaultTableModel.addRow(new Object[]{
                kh.getId(), kh.getMakh(), kh.getTenkh(), kh.getHokh(), kh.getNgaySinh(), kh.getSdt(), kh.getDiaChi(), kh.getThanhPho(), kh.getQuocGia(), kh.getMk()
            });
        }
    }

