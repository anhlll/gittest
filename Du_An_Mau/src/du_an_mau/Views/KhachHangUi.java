    private void tblkhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblkhMouseClicked
        // TODO add your handling code here:
        int row = tblkh.getSelectedRow();
        if (row == -1) {
            return;
        }
        String id = (String) tblkh.getValueAt(row, 0);
        String ma = (String) tblkh.getValueAt(row, 1);
        String ten = (String) tblkh.getValueAt(row, 2);
        String ho = tblkh.getValueAt(row, 3).toString();
        String ns = tblkh.getValueAt(row, 4).toString();
        String sdt = tblkh.getValueAt(row, 5).toString();
        String diachi = (String) tblkh.getValueAt(row, 6);
        String thanhpho = (String) tblkh.getValueAt(row, 7);
        String quocgia = (String) tblkh.getValueAt(row, 8);
        String mk = (String) tblkh.getValueAt(row, 9);

        txtid.setText(id);
        txtmakh.setText(ma);
        txttenkh.setText(ten);
        txtho.setText(ho);
        txtsdt.setText(sdt);
        txtmatkhau.setText(mk);
        txtns.setText(ns);
        txtthanhpho.setText(thanhpho);
        txtquocgia.setText(quocgia);
        txtdiachi.setText(diachi);
    }//GEN-LAST:event_tblkhMouseClicked

