    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        NhanVienViewModels n = new NhanVienViewModels();
        String ma = txtma.getText();
        String ten = txtten.getText();

        String ngaySinh = txtns.getText();
        String sdt = txtsdt.getText();
        String diaChi = txtdiachi.getText();
        String mk = txtmk.getText();
        ChucVu cv = (ChucVu) cbbcv.getSelectedItem();
        CuaHang ch = (CuaHang) cbbch.getSelectedItem();

        if (ma.isEmpty() | ten.isEmpty() | ngaySinh.isEmpty() | sdt.isEmpty() | diaChi.isEmpty() | mk.isEmpty()) {
            JOptionPane.showMessageDialog(this, "K dc de trong thong tin");
            return;
        }

        for (int i = 0; i < sdt.length(); i++) {
            if (!Character.isDigit(sdt.charAt(i))) {
                JOptionPane.showMessageDialog(this, "SDT phai la so");
                return;
            }
        }
        Pattern paternDate = Pattern.compile("\\d{2}[-|/]\\d{2}[-|/]\\d{4}");
        paternDate = Pattern.compile("^\\d{4}[-|/]\\d{2}[-|/]\\d{2}$");
        if (paternDate.matcher(txtns.getText()).matches() == true) {
            JOptionPane.showMessageDialog(this, "dung dinh dang ngay sinh");
        }
        if (paternDate.matcher(txtns.getText()).matches() == false) {
            JOptionPane.showMessageDialog(this, "sai dinh dang ngay sinh");
            return;
        }
        Date d = Date.valueOf(txtns.getText());

        cv.setId(cv.getId());
        n.setIdcv(cv);

        ch.setId(ch.getId());
        n.setIdch(ch);

        n.setManv(ma);
        n.setTennv(ten);
        ;
        if (rdonam.isSelected()) {
            n.setGt("Nam");
        } else {
            n.setGt("Nữ");
        }
        n.setSdt(sdt);
        n.setDiachi(diaChi);
        n.setMk(mk);
        n.setNs(d);
        if (cbbtt.getSelectedItem().equals("Còn làm")) {
            n.setTt(1);
        } else {
            n.setTt(0);
        }
        if (nhanVienService.insert(n)) {
            JOptionPane.showMessageDialog(this, "Thêm ok");
            loaddata(nhanVienService.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Thêm No");
        }
    }//GEN-LAST:event_btnthemActionPerformed

