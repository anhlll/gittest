    public void loaddcbbCv(ArrayList<ChucVu> list) {
        defaultComboBoxModel = (DefaultComboBoxModel) cbbcv.getModel();

        for (ChucVu cv : list) {
            defaultComboBoxModel.addElement(cv);
        }
    }

    public void loaddcbbtt() {
        ArrayList<String> list1 = new ArrayList<>();
        defaultComboBoxModel = (DefaultComboBoxModel) cbbtt.getModel();
        list1.add("Nghỉ việc");
        list1.add("Còn làm");

        for (String s : list1) {
            defaultComboBoxModel.addElement(s);
        }
    }

    public void loaddata(ArrayList<NhanVienViewModels> list) {
        defaultTableModel = (DefaultTableModel) tblnv.getModel();
        defaultTableModel.setRowCount(0);

        for (NhanVienViewModels nv : list) {
            defaultTableModel.addRow(new Object[]{
                defaultTableModel.getRowCount() + 1, nv.getManv(), nv.getTennv(),
                nv.getGt(), nv.getNs(), nv.getDiachi(), nv.getSdt(), nv.getMk(),
                nv.getTt() == 0 ? "Còn làm" : "Nghỉ việc", nv.getId()
            }
            );
        }
    }

