    private void tblnvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblnvMouseClicked
        // TODO add your handling code here:
        int row =tblnv.getSelectedRow();
        if(row ==-1){
            return;
        }
        
        String id =tblnv.getValueAt(row, 9).toString();
        String ma =tblnv.getValueAt(row, 1).toString();
        String ten =tblnv.getValueAt(row, 2).toString();
        String gt =tblnv.getValueAt(row, 3).toString();
        String ns =tblnv.getValueAt(row, 4).toString();
        String diachi =tblnv.getValueAt(row, 5).toString();
        String sdt =tblnv.getValueAt(row, 6).toString();
        String mk =tblnv.getValueAt(row, 7).toString();
        String tt =tblnv.getValueAt(row, 8).toString();
        
        defaultComboBoxModel=(DefaultComboBoxModel) cbbch.getModel();
        defaultComboBoxModel.setSelectedItem(nhanVienService.getList().get(row).getIdch());
        
        defaultComboBoxModel=(DefaultComboBoxModel) cbbcv.getModel();
        defaultComboBoxModel.setSelectedItem(nhanVienService.getList().get(row).getIdcv());
        
        txtid.setText(id);
        txtma.setText(ma);
        txtten.setText(ten);
        txtsdt.setText(sdt);
        txtmk.setText(mk);
        txtns.setText(ns);
        txtdiachi.setText(diachi);
        
        cbbtt.setSelectedItem(tt);
        
        if(gt.equalsIgnoreCase("Nam")){
            rdonam.setSelected(true);
        }else{
            rdonu.setSelected(true);
        }
        
                
       
        
    }//GEN-LAST:event_tblnvMouseClicked

